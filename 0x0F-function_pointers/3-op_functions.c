#include "3-calc.h"

/**
 * op_add - main entry point
 * @a: int #1
 * @b: int #2
 * Description: add two integers
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - main entry point
 * @a: int #1
 * @b: int #2
 * Description: subtract two integers
 * Return: difference between a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - main entry point
 * @a: int #1
 * @b: int #2
 * Description: multiplies two integers
 * Return: Product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - main entry point
 * @a: int #1
 * @b: int #2
 * Description: divedes two ints
 * Return: division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - main entry point
 * @a: int #1
 * @b: int #2
 * Description: remainder of division of a and b
 * Return: remainder of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
