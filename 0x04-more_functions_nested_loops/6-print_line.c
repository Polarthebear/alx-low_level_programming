#include "main.h"

/**
 * print_line - main entry point
 * @n: = print_line
 * Description: print '_' for each number of n
 */
void print_line(int n)
{
	char print_line = 'n';

	while (print_line == 'n')
	{
		if (print_line != 0 || print_line > 0)
		{
			_putchar("_");
		}
		_putchar("\n");
	}
	_putchar("\n");
}
